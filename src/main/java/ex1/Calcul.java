package ex1;

/**
 * Classe de calcul elementaire, addition, soustraction
 * @author Fork
 *
 */
public class Calcul {

	/**
	 * Addition de 2 entiers
	 * @param integer a
	 * @param integer b
	 * @return integer a+b
	 */
	public int getAddition(int a, int b){
		return a+b;
	}
	
	/**
	 * Soustraction de 2 entiers
	 * @param integer a
	 * @param integer b
	 * @return integer a-b
	 */
	public int getSoustraction(int a, int b){
		return a-b;
	}
}
